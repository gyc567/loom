"use strict";

const fs = require("node:fs");
const path = require("node:path");

function ensureLoomUserInstall(input) {
  const home = process.env.LOOM_HOME || path.join(process.env.HOME || "", ".loom");
  const binDir = path.join(home, "bin");
  const adaptersRoot = path.join(home, "adapters");
  const adapterRoot = path.join(adaptersRoot, input.adapter);
  const launcherPath = path.join(binDir, "loom-cli");
  const cliTarget = path.join(input.repoRoot, "dist", "cli.js");

  if (!fs.existsSync(cliTarget)) {
    throw new Error("dist/cli.js does not exist. Run npm run build first.");
  }

  fs.mkdirSync(binDir, { recursive: true });
  fs.mkdirSync(adapterRoot, { recursive: true });
  fs.chmodSync(cliTarget, 0o755);
  fs.writeFileSync(launcherPath, launcherSource({
    nodePath: process.execPath,
    cliTarget,
  }));
  fs.chmodSync(launcherPath, 0o755);

  const stamp = {
    loomHome: home,
    launcherPath,
    launcherRef: "$HOME/.loom/bin/loom-cli",
    cliTarget,
    adapter: input.adapter,
    adapterRoot,
    pluginInstallRoot: input.pluginInstallRoot ?? null,
    refreshedAt: new Date().toISOString(),
  };
  const stampPath = path.join(adapterRoot, "refresh.json");
  fs.writeFileSync(stampPath, `${JSON.stringify(stamp, null, 2)}\n`);
  return {
    ...stamp,
    stampPath,
  };
}

function launcherSource(input) {
  return [
    "#!/bin/sh",
    "# Generated by Loom plugin refresh. Do not edit by hand.",
    "if ! /bin/pwd >/dev/null 2>&1; then",
    "  cd \"${TMPDIR:-/tmp}\" 2>/dev/null || cd /tmp 2>/dev/null || :",
    "fi",
    `exec ${shellQuote(input.nodePath)} ${shellQuote(input.cliTarget)} "$@"`,
    "",
  ].join("\n");
}

function shellQuote(value) {
  return `'${String(value).replace(/'/g, "'\\''")}'`;
}

module.exports = {
  ensureLoomUserInstall,
};
